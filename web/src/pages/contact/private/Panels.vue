<script>
    import 'echarts/map/js/china.js'
    export default {
        name: "Panels",

        data (){
            return {
                carouselList : this.$store.state.contactImgList
            }
        },

        mounted() {
            this.drawLine();

            if (!(/msie [6|7|8|9]/i.test(navigator.userAgent))){
                console.log("new WOW()",new WOW());
                new WOW().init();
            }
        },
        methods : {
            drawLine (){
                var t = this.$echarts.init(document.getElementById("map-point"));

                var geoCoordMap = {"铜陵玉成软件":[117.826889,30.965973]};

                var convertData = function (data) {
                    var res = [];
                    for (var i = 0; i < data.length; i++) {
                        var geoCoord = geoCoordMap[data[i].name];
                        if (geoCoord) {
                            res.push({
                                name: data[i].name,
                                value: geoCoord.concat(data[i].value)
                            });
                        }
                    }
                    return res;
                };

                var u = {
                    backgroundColor: '#fff',
                    title: {
                        // text: '全国主要城市空气质量',
                        // subtext: 'data from PM25.in',
                        // sublink: 'http://www.pm25.in',
                        x:'center',
                        textStyle: {
                            color: '#fff'
                        }
                    },
                    tooltip: {
                        trigger: 'item',
                        formatter: function (params) {
                            return params.name + ' : ' + params.value[2];
                        }
                    },

                    geo: {
                        map: 'china',
                        label: {
                            emphasis: {
                                show: false
                            }
                        },
                        itemStyle: {
                            normal: {
                                areaColor: '#409eff',
                                borderColor: '#fff'
                            },
                            emphasis: {
                                areaColor: '#2a333d'
                            }
                        },
                        left: '0%',
                        zoom : 1
                    },
                    series: [
                        {
                            name: 'pm2.5',
                            type: 'scatter',
                            coordinateSystem: 'geo',
                            data: convertData([{name: "铜陵玉成软件", value:"铜陵中科大创业园A207"}]),
                            symbolSize: 12,
                            label: {
                                normal: {
                                    show: false
                                },
                                emphasis: {
                                    show: false
                                }
                            },
                            itemStyle: {
                                emphasis: {
                                    borderColor: '#fff',
                                    borderWidth: 1
                                }
                            }
                        }
                    ]
                };

                t.setOption(u);
                var myChart = this.$echarts.getInstanceByDom(document.getElementById("map-point"));
                window.onresize = function () {
                    myChart.resize();
                }
            }
        }
    }
</script>

